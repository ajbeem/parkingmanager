package com.pcentaury.parkingmanager.models;

public class Cajon {
	private Integer id;
	private Byte disponibilidad;
	private Integer id_piso;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Byte getDisponibilidad() {
		return disponibilidad;
	}
	public void setDisponibilidad(Byte disponibilidad) {
		this.disponibilidad = disponibilidad;
	}
	public Integer getId_piso() {
		return id_piso;
	}
	public void setId_piso(Integer id_piso) {
		this.id_piso = id_piso;
	}
	
}
